package beginnerjava;

public class IncrementDecrement {
    public static void main(String[] args) {
    
        int x = 20,
                y;
        
        y = ++x;
        System.out.println("Y = "+y);
        
        y = x++;
        System.out.println("Y = "+y);
        
        y = --x;
        System.out.println("Y = "+y);
        
        y = x--;
        System.out.println("Y = "+y);
        
        y = ++x;
        System.out.println("Y = "+y);
        
       
    }
    
}
